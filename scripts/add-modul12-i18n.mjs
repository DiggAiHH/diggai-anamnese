// ─── Merge all categories ────────────────────
function getAllKeysForLang(lang) {
  const result = {};
  // Waiting: full translations for all 11 langs
  const w = waiting[lang] || waiting.en;
  Object.assign(result, w);

  // Admin: de/en have full translations, rest use English
  const a = admin[lang] || admin.en;
  Object.assign(result, a);

  // ROI: de/en, rest English
  const r = roi[lang] || roi.en;
  Object.assign(result, r);

  // Wunschbox: de/en, rest English
  const wb = wunschbox[lang] || wunschbox.en;
  Object.assign(result, wb);

  // Permissions: de/en, rest English
  const p = permissions[lang] || permissions.en;
  Object.assign(result, p);

  return result;
}

// ─── Process all locale files ────────────────────
let totalAdded = 0;

for (const lang of LANGS) {
  const filePath = join(LOCALES_DIR, lang, 'translation.json');
  let existing;
  try {
    existing = JSON.parse(readFileSync(filePath, 'utf-8'));
  } catch {
    console.error(`  ⚠ Could not read ${filePath}`);
    continue;
  }

  const newKeys = getAllKeysForLang(lang);
  let added = 0;

  for (const [key, value] of Object.entries(newKeys)) {
    if (!(key in existing)) {
      existing[key] = value;
      added++;
    }
  }

  if (added > 0) {
    // Sort keys for consistency
    const sorted = {};
    for (const k of Object.keys(existing).sort()) {
      sorted[k] = existing[k];
    }
    writeFileSync(filePath, JSON.stringify(sorted, null, 2) + '\n', 'utf-8');
    console.log(`  ✅ ${lang}: added ${added} keys`);
    totalAdded += added;
  } else {
    console.log(`  ⏭ ${lang}: all keys already present`);
  }
}

console.log(`\n✅ Total: ${totalAdded} keys added across ${LANGS.length} locales.`);
