// ============ GENERATE TRANSLATION FILES ============
const targetLanguages = [
  { code: 'it', overrides: itOverrides, fallbackLang: en },
  { code: 'fr', overrides: frOverrides, fallbackLang: en },
  { code: 'pl', overrides: plOverrides, fallbackLang: en },
  { code: 'fa', overrides: faOverrides, fallbackLang: ar },
];

for (const { code, overrides, fallbackLang } of targetLanguages) {
  const result = {};
  const allKeys = Object.keys(de);

  for (const key of allKeys) {
    if (overrides[key] !== undefined) {
      result[key] = overrides[key];
    } else if (fallbackLang[key] && fallbackLang[key] !== de[key]) {
      result[key] = fallbackLang[key];
    } else {
      // For remaining keys, use EN translation if available
      if (en[key] && en[key] !== de[key]) {
        result[key] = en[key];
      } else {
        result[key] = de[key]; // keep German as last resort
      }
    }
  }

  // Add new keys
  for (const [nk, nv] of Object.entries(NEW_KEYS)) {
    result[nk] = nv[code] || nv.en;
  }

  // Write file
  const dir = path.join(localesDir, code);
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
  fs.writeFileSync(path.join(dir, 'translation.json'), JSON.stringify(result, null, 2), 'utf8');
  console.log(`${code.toUpperCase()}: ${Object.keys(result).length} keys (${Object.keys(overrides).length} explicit overrides)`);
}

